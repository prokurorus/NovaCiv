/**
 * Получить семена для языка
 */
function getSeeds(lang) {
  return seeds[lang] || seeds.en;
}

/**
 * Получить случайное семя (для тестирования)
 */
function getRandomSeed(lang) {
  const langSeeds = getSeeds(lang);
  if (langSeeds.length === 0) return null;
  return langSeeds[Math.floor(Math.random() * langSeeds.length)];
}

